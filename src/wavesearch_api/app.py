    # output tokens -- ingest is off the query-serving path, so it's fine to wait much longer.
    llm_enrich_timeout_seconds = max(5, int(os.environ.get("RETAIL_V2_LLM_ENRICH_TIMEOUT_SECONDS") or "60"))
